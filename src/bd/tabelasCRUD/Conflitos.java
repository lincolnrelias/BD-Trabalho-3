    private void updateOpLabel(){
        switch(cbTipo.getItemAt(cbTipo.getSelectedIndex())){
            case "Territorial":
                lbOp.setText("regiao");
                break;
            case "Religioso":
                lbOp.setText("religiao");
                break;
            case "Racial":
                lbOp.setText("etnia");
                break;
            case "Economico":
                lbOp.setText("matprima");
                break;
            default:
                lbOp.setText("regiao");
                break;
        }
    }
