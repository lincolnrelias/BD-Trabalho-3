    private void btnInsert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsert1ActionPerformed
        if(table.getRowCount()==0){
            JOptionPane.showMessageDialog(null,
            "N√£o foi poss√≠vel deletar",
            "Campos insuficientes para a opera√ß√£o",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int idDel=table.getSelectedRow()!=-1
                ?Integer.parseInt(table.getValueAt(table.getSelectedRow(), 0).toString())
                :Integer.parseInt(table.getValueAt(table.getRowCount()-1, 0).toString());
        try {
            Statement del = conn.createStatement();
            String sql = "Delete from conflito where codconflito="+idDel;
            del.execute(sql);
        } catch (SQLException ex) {
            Logger.getLogger(Conflitos.class.getName()).log(Level.SEVERE, null, ex);
        }
     
      selectAll();
        
    }//GEN-LAST:event_btnInsert1ActionPerformed

    private void btnInsert2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsert2ActionPerformed
        Integer id,oldId,nmortos,nferidos;
        String nome = '\''+tfNome.getText()+'\'';
        String tipo = cbTipo.getSelectedItem().toString();
        String oldTipo = table.getValueAt(table.getSelectedRow(), 2).toString();
        String op = '\''+tfOp.getText()+'\'';
        try{
            id = Integer.parseInt(tfID.getText());
            oldId = Integer.valueOf(table.getValueAt(table.getSelectedRow(), 0).toString());
            nmortos = Integer.parseInt(tfNMortos.getText());
            nferidos = Integer.parseInt(tfNFeridos.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,
            "Campos Inv√°lidos",
            "um campo num√©rico possui letras",
            JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            PreparedStatement sql = conn.prepareStatement("update conflito set codconflito="+id+","
                    + "nome="+nome+",nummortos="+nmortos+",numferidos="+nferidos+" where codconflito="+oldId);
            sql.executeUpdate();
            sql = conn.prepareStatement("update conflito set tipoconf='"+tipo+"' where codconflito="+id);
            sql.execute();
            sql = conn.prepareStatement("delete from "+oldTipo+" where codconflito="+id);
            sql.execute();
            sql = conn.prepareStatement("Insert into "+tipo+" values("+id+","+op+")");
            sql.executeUpdate();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,
            "ID n√£o pode ser repetido",
            "Campos Inv√°lidos",
            JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Conflitos.class.getName()).log(Level.SEVERE, null, ex);
        }
        selectAll();
    }//GEN-LAST:event_btnInsert2ActionPerformed

